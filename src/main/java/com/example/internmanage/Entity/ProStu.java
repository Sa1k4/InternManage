package com.example.internmanage.Entity;

public class ProStu {
    private int id;
    private int stu_id;
    private int pro_id;
    private int apply_com;
    private int del;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getStu_id() {
        return stu_id;
    }

    public void setStu_id(int stu_id) {
        this.stu_id = stu_id;
    }

    public int getPro_id() {
        return pro_id;
    }

    public void setPro_id(int pro_id) {
        this.pro_id = pro_id;
    }

    public int getApply_com() {
        return apply_com;
    }

    public void setApply_com(int apply_com) {
        this.apply_com = apply_com;
    }

    public int getDel() {
        return del;
    }

    public void setDel(int del) {
        this.del = del;
    }
}
